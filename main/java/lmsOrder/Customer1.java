package lmsOrder;

public class Customer1 {

	
		private String name;
		private String address;
		private String no;
		private String date;
		private String time;
		private String urgency;
		private String quantity;
		private String amount;
		private String id;
	
		
		
		


		public Customer1( String name, String address, String no, String date, String time, String urgency,
				String quantity, String amount, String id) {
			this.name = name;
			this.address = address;
			this.no = no;
			this.date = date;
			this.time = time;
			this.urgency = urgency;
			this.quantity = quantity;
			this.amount = amount;
			this.id =id;
		}



		public String getName() {
			return name;
		}



		public String getAddress() {
			return address;
		}



		public String getNo() {
			return no;
		}



		public String getDate() {
			return date;
		}



		public String getTime() {
			return time;
		}



		public String getUrgency() {
			return urgency;
		}



		public String getQuantity() {
			return quantity;
		}



		public String getAmount() {
			return amount;
		}
		
		public String getId() {
			return id;
		}


		
		
}

